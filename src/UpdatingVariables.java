public class UpdatingVariables {
    public static void main(String[] args) {
        // Ints
        int salary = 1000;
        // Update 1
        salary = salary + 500;
        // Update 2
        salary -= 200;
        System.out.println(salary);

        // Strings
        String employeeName = "Bryan Enrique";
        employeeName = employeeName + " Garay Benavidez";
        System.out.println("Mi nombre es "+employeeName);
    }
}
