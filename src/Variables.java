public class Variables {
    public static void main(String[] args) {
        // Declarar variable
        int speed;
        // Inicializamos la variable
        speed = 10;
        System.out.println(speed);

        int salary = 1000;
        System.out.println(salary);

        String employeeName = "Mana";
        System.out.println(employeeName);
    }
}
